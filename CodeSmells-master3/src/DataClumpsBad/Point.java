

package DataClumpsBad;

import DataClassBad.*;


public class Point {
	
	private double X;
	private double Y;
	
	public Point(double x, double y){
		this.X = x;
		this.Y = y;
		
	}

	public double getCoordX() {
		return X;
	}

	public void setCoordX(double X) {
		this.X = X;
	}

	public double getCoordY() {
		return Y;
	}

	public void setCoordY(double Y) {
		this.Y = Y;
	}
	
	
}
